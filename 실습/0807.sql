--15.EXTRACT(YEAR FROM )
SELECT EMP_NAME, EXTRACT(YEAR FROM TO_DATE(SUBSTR(EMP_NO, 1 , 6))),
EXTRACT(MONTH FROM TO_DATE(SUBSTR(EMP_NO, 1 , 6))),
EXTRACT(DAY FROM TO_DATE(SUBSTR(EMP_NO, 1 , 6)))
FROM EMPLOYEE;

--16.
SELECT EMP_NAME, RPAD(SUBSTR(EMP_NO, 1, INSTR(EMP_NO, '-')), LENGTH(EMP_NO), 'X')
FROM EMPLOYEE;

--17.
SELECT EMP_NAME, ABS(TRUNC(HIRE_DATE - SYSDATE)) AS 근무일수1, TRUNC(SYSDATE - HIRE_DATE) AS 근무일수2
FROM EMPLOYEE;

--18.
