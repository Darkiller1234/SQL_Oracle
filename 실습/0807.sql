SELECT HIRE_DATE, SALARY * 12 AS 연봉, SALARY * (NVL(BONUS, 0) + 1) * 12 AS 총수령액,
       (SALARY * (NVL(BONUS, 0) + 1) * 12) * 0.97 AS 실수령액
