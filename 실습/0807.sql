--25.

SELECT COUNT(EMP_ID) AS "전체 직원 수", 
COUNT(CASE WHEN EXTRACT(YEAR FROM HIRE_DATE) = 2001 THEN '1'
            ELSE NULL
            END) AS "2001년",
    COUNT(CASE WHEN EXTRACT(YEAR FROM HIRE_DATE) = 2002 THEN '2'
            ELSE NULL
            END) AS "2002년",
    COUNT(CASE WHEN EXTRACT(YEAR FROM HIRE_DATE) = 2003 THEN '3'
            ELSE NULL
            END) AS "2003년",
    COUNT(CASE WHEN EXTRACT(YEAR FROM HIRE_DATE) = 2004 THEN '4'
            ELSE NULL
            END) AS "2004년"
FROM EMPLOYEE;